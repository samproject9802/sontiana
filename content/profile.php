<h2 class="card-title mb-5 text-center">Profile Perusahaan</h2>
<p class="card-text px-5" style="font-size: 14pt; text-align: justify;">
    UD Harapan Seni Tani merupakan perusahaan yang bergerak di bidang pertanian,
    bukan hanya menjual pupuk saja namun menjual segala kebutuhan yang di perlukan
    bagi para petani baik itu, pupuk ,obat-obatan, pestisida, dan lain sebagainya.
    Perusahaan ini sudah berdiri sejak 19 september 2000 dan masih aktif sampai sekarang.
    Perusahaan ini terletak di Kabanjahe, perusahaan ini merupakan salah satu pemasok
    terbesar bahan pertanian di kota itu, bahkan perusahaan ini juga menjadi pemasok
    pupuk di daerah lain contohnya : Pekan Baru, Nias, Kabupaten Simalungun dan lain
    sebagainya.
</p>